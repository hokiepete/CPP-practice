#include <iostream>
using namespace std;

// Enter your code for reversed_binary_value<bool...>()
template<bool in1> int reversed_binary_value(){
    return in1;
}

template<bool in1, bool in2, bool... in3> int reversed_binary_value(){
    return (reversed_binary_value<in2, in3...>()<<1)+in1;
}

template <int n, bool...digits>
struct CheckValues {
  	static void check(int x, int y)
  	{
    	CheckValues<n-1, 0, digits...>::check(x, y);
    	CheckValues<n-1, 1, digits...>::check(x, y);
  	}
};

template <bool...digits>
struct CheckValues<0, digits...> {
  	static void check(int x, int y)
  	{
    	int z = reversed_binary_value<digits...>();
    	std::cout << (z+64*y==x);
  	}
};

int main()
{
  	int t; std::cin >> t;

  	for (int i=0; i!=t; ++i) {
		int x, y;
    	cin >> x >> y;
    	CheckValues<6>::check(x, y);
    	cout << "\n";
  	}
}
